#!/usr/bin/env python3
""" This module define the function concat(). """


def concat(str1: str, str2: str) -> str:
    """ Concatenates 'str1' with 'str2' and return this one.

    Args:
        str1 (str): First string.
        str2 (str): Second string.

    Returns:
        str: Concatenation of str1 + str2.
    """
    return (str1 + str2)
